package com.lookation.dto;

public class SampleDTO
{
	// 객체로 처리할 데이터 선언
	private String a, c;
	private int b, d;
	public String getA()
	{
		return a;
	}
	public void setA(String a)
	{
		this.a = a;
	}
	public String getC()
	{
		return c;
	}
	public void setC(String c)
	{
		this.c = c;
	}
	public int getB()
	{
		return b;
	}
	public void setB(int b)
	{
		this.b = b;
	}
	public int getD()
	{
		return d;
	}
	public void setD(int d)
	{
		this.d = d;
	}
	
	
}

package com.lookation.dto;

public class packageInfoDTO
{
	private String package_code, package_form_code, package_name;
	private int package_start, package_end, package_price;
	
	public String getPackage_code()
	{
		return package_code;
	}
	public void setPackage_code(String package_code)
	{
		this.package_code = package_code;
	}
	public String getPackage_form_code()
	{
		return package_form_code;
	}
	public void setPackage_form_code(String package_form_code)
	{
		this.package_form_code = package_form_code;
	}
	public String getPackage_name()
	{
		return package_name;
	}
	public void setPackage_name(String package_name)
	{
		this.package_name = package_name;
	}
	public int getPackage_start()
	{
		return package_start;
	}
	public void setPackage_start(int package_start)
	{
		this.package_start = package_start;
	}
	public int getPackage_end()
	{
		return package_end;
	}
	public void setPackage_end(int package_end)
	{
		this.package_end = package_end;
	}
	public int getPackage_price()
	{
		return package_price;
	}
	public void setPackage_price(int package_price)
	{
		this.package_price = package_price;
	}
	
}

package com.lookation.dto;

public class ReviewDTO
{
	private String review_code, loc_code, member_code, review_rate
	, review_date, review_content
	, review_remove_date;

	public String getReview_code()
	{
		return review_code;
	}

	public void setReview_code(String review_code)
	{
		this.review_code = review_code;
	}

	public String getLoc_code()
	{
		return loc_code;
	}

	public void setLoc_code(String loc_code)
	{
		this.loc_code = loc_code;
	}

	public String getMember_code()
	{
		return member_code;
	}

	public void setMember_code(String member_code)
	{
		this.member_code = member_code;
	}

	public String getReview_rate()
	{
		return review_rate;
	}

	public void setReview_rate(String review_rate)
	{
		this.review_rate = review_rate;
	}

	public String getReview_date()
	{
		return review_date;
	}

	public void setReview_date(String review_date)
	{
		this.review_date = review_date;
	}

	public String getReview_content()
	{
		return review_content;
	}

	public void setReview_content(String review_content)
	{
		this.review_content = review_content;
	}

	public String getReview_remove_date()
	{
		return review_remove_date;
	}

	public void setReview_remove_date(String review_remove_date)
	{
		this.review_remove_date = review_remove_date;
	}
	
}
